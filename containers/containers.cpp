// Iterators:

// begin(): Returns an iterator pointing to the first element of the vector.
// end(): Returns an iterator pointing one past the last element of the vector.
// rbegin(): Returns a reverse iterator pointing to the last element.
// rend(): Returns a reverse iterator pointing to one position before the first element.
// Capacity:

// size(): Returns the number of elements in the vector.
// max_size(): Returns the maximum number of elements the vector can hold.
// capacity(): Returns the number of elements that the vector can currently hold without reallocation.
// resize(size_type n): Resizes the container to contain n elements.
// empty(): Checks if the vector is empty.
// Element access:

// operator[]: Accesses the element at a specified position.
// at(): Accesses the element at a specified position with bounds checking.
// front(): Returns a reference to the first element.
// back(): Returns a reference to the last element.
// data(): Returns a pointer to the underlying array serving as element storage.
// Modifiers:

// push_back(): Adds an element to the end of the vector.
// pop_back(): Removes the last element from the vector.
// insert(): Inserts elements at a specified position.
// erase(): Removes elements from a specified position.
// clear(): Removes all elements from the vector.
// swap(): Swaps the contents of two vectors.